            // Voorbeeld van een AJAX-verzoek met jQuery
            $.ajax({
                url: 'addToCart.php', // Verander dit naar het juiste pad van je PHP-script
                type: 'POST',
                data: { itemId: $(this).data('itemid') }, // Stuur het item-ID mee, als dat nodig is
